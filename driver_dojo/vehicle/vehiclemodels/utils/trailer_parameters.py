class TrailerParameters:
    # class for trailer parameters
    def __init__(self):
        self.l = None           # trailer length [m]
        self.w = None           # trailer width [m]
        self.l_hitch = None     # hitch length [m]
        self.l_total = None     # total system length [m]
        self.l_wb = None        # trailer wheel base [m]
